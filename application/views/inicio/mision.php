
  <h2 class="text-info">MISION</h2>
  <p class="text-left">
    Impulsar el desarrollo de la investigación vinculándola a los procesos  de  formación  y de la proyección social. Así como el desarrollo de estudios avanzados acorde a los retos que plantean las dinámicas de cambio propias de nuestro país.
</p><br>
<h3 class="text-info">
 Políticas
</h3>
<p class="text-left">
  1 Consolidar  la  carrera académica de los profesores y estudiantes sustentada en  el desarrollo de la investigación institucional, con base en programas y líneas de investigación específica para  cada región.
</p>
<p class="text-left">
  2 Promover una  investigación articulada a la docencia y a la inserción social como eje organizador de los programas académicos.
</p>
<p class="text-left">
  3 Impulsar el nivel de formación avanzada
</p>
</p><br>
<h3 class="text-info">Objetivo </h3>
<p class="text-left">
 Desarrollar y sistematizar  la profesionalización de los profesores y estudiantes sustentada en el desarrollo de la investigación institucional, con base en programas y líneas de investigación específica para  cada región.
</p><br>
<h3 class="text-info">
Metas
</h3>
<p class="text-left">
<strong>• </strong> Formación continúa de los profesores  mediante cursos de formación avanzada, asistencia a congresos y otros eventos de carácter académico, pasantías o cualquier otra forma de movilidad académica.
</p>
<p class="text-left">
<strong>• </strong> Fomentar la articulación de los docente-estudiantes-comunidades con redes, programas y actividades de investigación  regional, nacional e internacional.
</p>
<p class="text-left">
<strong>• </strong> Creación del Centro de Investigación y Formación Avanzada, de acuerdo al perfil de la Universidad y la realidad social-económica-territorial y política de la región.
</p>
