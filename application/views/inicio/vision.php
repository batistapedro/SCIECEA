<h2 class="text-info">VISION</h2>
<p class="text-left">
  Propulsar el desarrollo académico como una vía para la educación a lo largo de la vida, a través de la fusión de las diversas actividades académicas que generen un aporte pertinente, sustancial y relevante al compromiso de materializar la propuesta política, social y económica que supone el desarrollo endógeno en aras de consolidar la nueva estructura del Estado.
</p><br>

<h3 class="text-info">PROPÓSITOS o FUNCIONES</h3>
<p class="text-left">
Orientar investigaciones que fortalezcan el desarrollo local, regional, nacional e internacional en aspectos asociados a los Programas de Formación Avanzada.
Impulsar la vinculación con los sectores sociales, públicos e institucionales destacando los puntos de encuentro entre éstos, a través de Proyectos de Investigación con resultados compartidos.
Fomentar la formación de investigadores en los campos del conocimiento que sean competencia del Programa de Formación Avanzada.
Propiciar el desarrollo de  los Proyectos de Investigación y la consolidación de los Estudios Avanzados mediante acuerdos de integración con otras instituciones, aprovechando las oportunidades y beneficios que ofrece el trabajo interdisciplinario.
</p><br>

<h3 class="text-info">OBJETIVO GENERAL</h3>

<p class="text-left">
Estimular la discusión teórica y ofrecer oportunidades para el estudio de los procesos sociales, económicos, políticos, ambientales, tecnológicos y culturales que manifiestan las comunidades e instituciones del Estado, además de las posibles soluciones a las diversas problemáticas planteadas en éstas áreas, en el marco de las estrategias para la conformación de la nueva institucionalidad del Estado venezolano.
</p><br>

<h3 class="text-info">OBJETIVOS ESPECÍFICOS</h3>

<p class="text-left">
Suministrar los conocimientos teóricos y metodológicos para la revisión y crítica de las políticas públicas y su adecuación a su eficiente prestación de los servicios que estas políticas generan.
Formular, diseñar, gestionar y evaluar procesos relacionados con la prestación de servicios públicos, bajo estándares de excelencia.
Generar soluciones a los problemas que plantea la realidad social, local, comunitaria, institucional y nacional en permanente y acelerado cambio.
</p>
